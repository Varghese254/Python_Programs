#string
str="hello my WORLD and my dear "
print(str)
#convert into lower
print(str.lower())

#convert into upper
print(str.upper())

#change the case or swap b/w them
print(str.swapcase())

print(str.capitalize())
print(str.title())

#to replace a string with another string
print(str.replace("my","our"))

#if there is more than 1 same words in the string
print(str.replace("my","our",1))

#to count character in a string
print(str.count("d"))

#cout b/w two range
print(str.count("e",0,30))


#index
print(str.index("e"))
